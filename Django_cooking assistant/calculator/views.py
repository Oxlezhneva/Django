def home_view(request, param):
    template_name = 'calculator/index.html'
    count = int(request.GET.get("servings",1))
    my_dict = {}
    for i in DATA[str(param)].items():
        next = i[1]*count
        my_dict[i[0]] = next    
    context = {
    'recipe': my_dict 
    }
    return render(request, template_name, context)
